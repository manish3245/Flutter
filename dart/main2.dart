void main() {
  //boolean => true or false

  int num = 9;
  bool isGreaterThan0 = num > 10;
  bool isLessThan0 = num < 10;

  print(isGreaterThan0);
  print(isLessThan0);

  //conditional operators
  // <, >, <=, >=, ==, !=,

  int firstNumber = 30;
  int secondNumber = 40;

  bool sum = firstNumber != secondNumber;
  print(sum);
}
